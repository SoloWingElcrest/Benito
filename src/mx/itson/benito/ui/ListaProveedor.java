    public void cargar(){
        List<Proveedor> proveedores = ProveedorDAO.obtenerTodos();
        DefaultTableModel model = (DefaultTableModel) tblProveedores.getModel();
        model.setRowCount(0);
        for(Proveedor p : proveedores){
            model.addRow(new Object[] {
                p.getIdproveedor(),
                p.getNombre(),
                p.getDireccion(),
                p.getTelefono(),
                p.getEmail(),
                p.getContacto()
            });
        }
    }
