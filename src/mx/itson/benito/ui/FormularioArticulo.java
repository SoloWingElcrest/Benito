    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
        String nombre = txtNombre.getText();
        double precio = Double.parseDouble(txtPrecio.getText());
        Proveedor proveedor = (Proveedor)cmbProveedor.getSelectedItem();
        System.out.println("El proveedor seleccionado es: " + proveedor.getNombre());
        
        boolean resultado = this.idArticulo == 0 ?
        
        ArticuloDAO.guardar(nombre, precio, proveedor):
        ArticuloDAO.editar(idArticulo, nombre, precio, proveedor);
        
        if(resultado){
            JOptionPane.showMessageDialog(this, "El registro se guard√≥ correctamente", "Registro guardado", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }else
                JOptionPane.showMessageDialog(this, "El registro se edito con exito", "Registro guardado", JOptionPane.INFORMATION_MESSAGE);
                dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurri√≥ un error al guardar", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

